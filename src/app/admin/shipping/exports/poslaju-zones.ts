// Pulang tier Poslaju (D1/D2) untuk poskod, atau null kalau bukan D1-D2 Semenanjung.
export function poslajuTier(postcode: string | null | undefined): 'D1' | 'D2' | null {
  if (!postcode) return null
  const n = parseInt(String(postcode).replace(/\D/g, ''), 10)
  if (!Number.isFinite(n)) return null
  for (const [from, to, tier] of ZONES) {
    if (n >= from && n <= to) return tier
  }
  return null
}
